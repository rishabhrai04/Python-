L1=[1,8,7,2,21,15]
L1.sort()

print(L1) # sort the list in ascending order
L1.reverse() # reverse the list
print(L1) # reverse the list
L1.append(100) # append 100 to the end of the list
print(L1) # append 100 to the end of the list
L1.insert(0, 200) # insert 200 at index 0
print(L1) # insert 200 at index 0
L1.remove(21) # remove 200 from the list
print(L1) # remove 200 from the list
L1.pop() # remove the last element from the list
print(L1) # remove the last element from the list
L1.pop(0) # remove the first element from the list
print(L1) # remove the first element from the list